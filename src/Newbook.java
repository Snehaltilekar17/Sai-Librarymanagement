            // TODO add your handling code here:
            
            String B_id =Book_id.getText();
            String Name =txtBname.getText();
            String Publisher =txtPublisher.getText();
            String Price =txtPrice.getText();
            String Year =txtYear.getText();
            
            
  
           
            conn = DriverManager.getConnection("jdbc:mysql://localhost/sailibrary","root","");
            insert = conn.prepareStatement("insert into books(b_id,name,publisher,price,p_year)values(?,?,?,?,?)");
            insert.setString(1, B_id);
            insert.setString(2, Name);
            insert.setString(3, Publisher);
            insert.setString(4, Price);
            insert.setString(5, Year);

            insert.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Record Added");
            
