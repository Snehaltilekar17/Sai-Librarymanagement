    public void autoID()
    {
    try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/sailibrary", "root", "");
         Statement s = conn.createStatement()) {
        
        ResultSet rs = s.executeQuery("SELECT MAX(B_id) FROM books");
        if (rs.next()) {
            String maxId = rs.getString(1);
            if (maxId == null) {
                Book_id.setText("BK001");
            } else {
                long id = Long.parseLong(maxId.substring(2)) + 1;
                Book_id.setText("BK" + String.format("%03d", id));
            }
        }
    } catch (SQLException ex) {
        Logger.getLogger(Newbook.class.getName()).log(Level.SEVERE, "Error in autoID", ex);
    } catch (NumberFormatException ex) {
        Logger.getLogger(Newbook.class.getName()).log(Level.SEVERE, "Error parsing ID", ex);
    }

        
    }      
    
    
